int gameboard[21][14]; // 게임판을 구현한 배열

int main() {
	init();
	total_gameboard();
}

int init() {
	int i, j;

	for (i = 0; i < 20; i++) {
		for (j = 0; j < 14; j++) {
			if ((j == 0) || (j == 13)) {
				gameboard[i][j] = 1;
			}
			else {
				gameboard[i][j] = 0;
			}
		}
	}

	for (j = 0; j < 14; j++) {
		gameboard[20][j] = 1;
	}

	return 0;
}

int total_gameboard() {
	for (int i = 0; i < 21; i++) {
		for (int j = 0; j < 14; j++) {
			if (gameboard[i][j] == 1)
				printf("■");
			else
				printf("  ");
		}
		printf("\n");
	}

	return 0;
}